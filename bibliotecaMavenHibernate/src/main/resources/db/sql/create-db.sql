create table AUTORES 
(
  ID INTEGER PRIMARY KEY GENERATED BY DEFAULT AS identity (START WITH 1, INCREMENT BY 1),
  NOMBRE VARCHAR (100) not null,
  NACIONALIDAD VARCHAR (50) not null,
  COMENTARIOS VARCHAR (200)
  )
;

create table DIRECCIONES 
(
  ID INTEGER PRIMARY KEY GENERATED BY DEFAULT AS identity (START WITH 1001, INCREMENT BY 1),
  CALLE VARCHAR (100),
  NUMERO VARCHAR (10),
  POBLACION VARCHAR (50),
  CP VARCHAR(10),
  PROVINCIA VARCHAR (30)
)
;

  
create table EDITORIALES
(
  NIF VARCHAR (20) PRIMARY KEY,
  NOMBRE VARCHAR (50) NOT NULL,
  DIRECCION1 INTEGER CONSTRAINT DIRECCION_FK REFERENCES DIRECCIONES
)
;
  
  
create table LIBROS
(
  ISBN VARCHAR (20) PRIMARY KEY,
  TITULO VARCHAR (50),
  EDITORIALES1 VARCHAR (20) CONSTRAINT EDITORIAL_FK REFERENCES EDITORIALES,
  AUTORES1 INTEGER CONSTRAINT AUTOR_FK REFERENCES AUTORES,
  ANO INTEGER,
  PRECIO NUMERIC (5,2),
  DESCRIPCION VARCHAR (300)
)
;

create table LIBROS_AUTORES
(
	ID_AUTORES INTEGER not null CONSTRAINT AUTORES_ID_FK REFERENCES AUTORES,
	ID_LIBROS VARCHAR (20) not null CONSTRAINT LIBROS_ID_FK REFERENCES LIBROS,
	primary key (ID_AUTORES, ID_LIBROS)
)
;